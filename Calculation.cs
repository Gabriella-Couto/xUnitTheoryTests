using System;
using System.Collections.Generic;
using System.Text;

namespace Calculations
{
    public class Calculation
    {
        public bool isOddNumber (int n)
        {
            return (n % 2) == 1;
        }

        public int sumTwoValues(int n1, int n2)
        {
            return n1 + n2;
        }

    }
}
